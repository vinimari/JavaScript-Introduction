// There actually three ways to declare a variable in Javascript 

// 1 - var
// Changeable. Is going to be able to be used throughout your whole program 
var myName = "Vini";
var difference = 45 - 3; 

// 2 - let 
// Only be used within the scope of where you declared that 
let ourName = "Vini and Mari";

// 3 - const
// Never change 
const pi = 3.14; 

